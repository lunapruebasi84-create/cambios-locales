export const permissionKeys: PermissionKey[] = permissionCatalog.map(
  (permission) => permission.key,
);

export const permissionCatalogByKey = permissionCatalog.reduce(
  (acc, permission) => {
    acc[permission.key] = permission;
    return acc;
  },
  {} as Record<PermissionKey, PermissionDefinition>,
);

export const permissionsByModule = permissionCatalog.reduce(
  (acc, permission) => {
    if (!acc[permission.module]) {
      acc[permission.module] = [];
    }

    acc[permission.module].push(permission);
    return acc;
  },
  {} as Record<string, PermissionDefinition[]>,
);